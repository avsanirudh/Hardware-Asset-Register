    public static String getMD5(String input) 
    { 
        try { 
 
            MessageDigest md = MessageDigest.getInstance("MD5"); 

            byte[] messageDigest = md.digest(input.getBytes()); //digest()returns the message digest of the input to byte array
 
            BigInteger no = new BigInteger(1, messageDigest); //Byte array to signum representation
  
            String hashtext = no.toString(16); //message digest to hexa val
            while (hashtext.length() < 32) 
            { 
                hashtext = "0" + hashtext; 
            } 
            return hashtext; 
        }  
  
        catch (NoSuchAlgorithmException e) { 
            throw new RuntimeException(e); 
        } 
    }
    
