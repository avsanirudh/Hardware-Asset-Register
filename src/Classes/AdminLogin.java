    public void logout()
    {
        try
        {
        String uid = "";
            con = new DesignHome().dbconn();
            String que = "Select userid, sno from ActivityLog order by Sno desc limit 1";
            stmt = con.createStatement();
            rs = stmt.executeQuery(que);
            int sno = 0;
            while(rs.next())
            {
                uid = rs.getString(1);
                sno = rs.getInt(2);
            }

            System.out.println("UserID = " + uid);
            
            String query = "update ActivityLog set logout = ? where sno = '" + sno + "';";
            pstmt = con.prepareStatement(query);

            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
            java.util.Date date = new java.util.Date();
            sdf.format(date);
            String datetime = date.toString();

            pstmt.setString(1, datetime);
            pstmt.executeUpdate();
            
            stmt.close();
            pstmt.close();
            rs.close();
            con.close();
            
            new Home().setVisible(true);
            this.dispose();
        }
    
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    public void clock()
    {
        Thread clock = new Thread()
        {
            public void run()
            {
                try 
                {
                    for(;;)
                    {
                        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
                        java.util.Date date = new java.util.Date();
                        ClockLbl.setText(sdf.format(date)); 
                        sleep(1000);
                    }
                }
                catch (InterruptedException ex) 
                {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }        
        };
        clock.start();
    }
    
    
    public void logincheck()
    {
        String uname = UserNameTxt.getText();
        String password = new String(PassTxt.getPassword());
        String encpass = getMD5(password);
        String dbpass = "";
        String Isadmin="";
        
        if(uname.equals("") || password.equals(""))
            JOptionPane.showMessageDialog(null, "None Of The Fields Can Be Empty");
        
        else
        {
            try
            {
                con = new DesignHome().dbconn();
                con.setAutoCommit(false);

                String query = "Select password from users where username = '" + uname + "'";
                stmt = con.createStatement();
                rs = stmt.executeQuery(query);
                
                stmt1 = con.createStatement();
                String query1 = "select isadmin from users where username = '" + uname + "'";
                rs1 = stmt1.executeQuery(query1);
                if(rs.next())
                    {
                        dbpass = rs.getString("password");
                        if(rs1.next())
                        {
                            Isadmin = rs1.getString(1);
                        }
                    }
                if(dbpass.equals(encpass))
                    {
                        if(Isadmin.equals("True"))
                        {
                        new AdminMainMenu().setVisible(true);
                        this.dispose();
                        }
                        else
                        JOptionPane.showMessageDialog(null, "User does not have admin rights");
                    }
                else
                JOptionPane.showMessageDialog(null, "Invalid credentials");
            }
            
            catch(SQLException E)
            {
                JOptionPane.showMessageDialog(null, E.getMessage());
            }
        }

    }
    
